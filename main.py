# Требуется получить центральность для 20 узлов с «ямой» и «горбиком» значений,
# т. е. когда последовательность сначала падает вниз, потом возрастает до пикового значения,
# а затем снова опускается.

import networkx as nx

# Создание графа
G = nx.path_graph(20)

# Добавляем реберо для создания "ямок" и "горбиков" в центральности
G.add_edge(0, 10)

# Вычисление центральности
centrality = nx.eigenvector_centrality(G, max_iter=200)

# Вывод центральностей
print("Центральности узлов:")
for node, value in centrality.items():
    print(f"Узел {node}: Центральность {value:.4f}")